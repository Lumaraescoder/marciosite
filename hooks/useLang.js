"use client";
import { useState, useEffect, createContext, useContext } from 'react';

const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState('en');
  const [isDetecting, setIsDetecting] = useState(true);

  // 🔍 DETECÇÃO AUTOMÁTICA DO BROWSER - MAIS AGRESSIVA
  useEffect(() => {
    console.log('🔍 INICIANDO DETECÇÃO DE LÍNGUA...');

    const detectBrowserLanguage = () => {
      // FORÇA ignorar localStorage para teste
      // const savedLang = localStorage.getItem('language');
      // if (savedLang) {
      //   console.log('📁 Língua salva IGNORADA para teste:', savedLang);
      //   // return; // Comenta esta linha para forçar detecção
      // }

      const browserLang = navigator.language || navigator.userLanguage || 'en';
      console.log('🌐 Língua CRUA do browser:', browserLang);

      // Mapeamento SIMPLIFICADO
      const langMap = {
        'pt': 'pt', 'pt-BR': 'pt', 'pt-PT': 'pt',
        'es': 'es', 'es-ES': 'es', 'es-MX': 'es',
        'fr': 'fr', 'fr-FR': 'fr', 'fr-CA': 'fr',
        'de': 'de', 'de-DE': 'de',
        'it': 'it', 'it-IT': 'it',
        'en': 'en', 'en-US': 'en', 'en-GB': 'en'
      };

      const baseLang = browserLang.split('-')[0];
      const detectedLang = langMap[browserLang] || langMap[baseLang] || 'en';

      console.log('🎯 Língua FINAL detectada:', detectedLang);
      console.log('📍 Browser language:', browserLang);
      console.log('📍 Base language:', baseLang);

      return detectedLang;
    };

    const detectedLang = detectBrowserLanguage();
    setLang(detectedLang);
    setIsDetecting(false);

  }, []);

  // 💾 Salva no localStorage quando mudar (mas não durante detecção)
  useEffect(() => {
    if (!isDetecting) {
      localStorage.setItem('language', lang);
      console.log('💾 Língua salva no localStorage:', lang);
    }
  }, [lang, isDetecting]);

  return (
    <LanguageContext.Provider value={{ lang, setLang }}>
      {children}
    </LanguageContext.Provider>
  );
}

